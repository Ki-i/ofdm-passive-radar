function [zk]=DABphaseSymModeI(kin)
K=1536;
zk=zeros(size(kin(:)));
ind=1;
for k=kin(:).'
if (k==0) || (k>K/2) || (k<(-K/2))
    zk(ind)=0;
else
tminT=[(-24:-1)*32 (0:23)*32+1];
tmaxT=[(-23:0)*32-1 (1:24)*32];
ksT=[(-24:-1)*32 (0:23)*32+1];
iT=mod([-24:-1,-(0:23)],4);
nT=[1,2,0,1,3,2,2,3,2,1,2,3,1,2,3,3,2,2,2,1,1,3,1,2,3,1,1,1,2,2,1,0,2,2,3,3,0,2,1,3,3,3,3,0,3,0,1,1];
h0=[0,2,0,0,0,0,1,1,2,0,0,0,2,2,1,1,0,2,0,0,0,0,1,1,2,0,0,0,2,2,1,1];
h1=[0,3,2,3,0,1,3,0,2,1,2,3,2,3,3,0,0,3,2,3,0,1,3,0,2,1,2,3,2,3,3,0];
h2=[0,0,0,2,0,2,1,3,2,2,0,2,2,0,1,3,0,0,0,2,0,2,1,3,2,2,0,2,2,0,1,3];
h3=[0,1,2,1,0,3,3,2,2,3,2,1,2,1,3,2,0,1,2,1,0,3,3,2,2,3,2,1,2,1,3,2];
h=[h0;h1;h2;h3];


index=((k>=tminT) & (k<=tmaxT));
ks=ksT(index);
i=iT(index);
n=nT(index);

phik=pi/2*(h(i+1,k-ks+1)+n);
zk(ind)=exp(1i*phik);

end
ind=ind+1;
end
end


